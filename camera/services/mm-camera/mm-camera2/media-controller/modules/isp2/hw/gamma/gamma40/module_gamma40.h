  uint8_t     gamma_r[ISP_GAMMA_NUM_ENTRIES];
  uint8_t     gamma_g[ISP_GAMMA_NUM_ENTRIES];
  uint8_t     gamma_b[ISP_GAMMA_NUM_ENTRIES];
